// ============================================================================
// CALCULATE SECTOR PIPELINE - Auto-calculated from opportunities
// ============================================================================
export interface SectorPipeline {
  sector: string
  pipeline: number
  projects: number
  color: string
  quality: 'leading-edge' | 'advanced' | 'mature' | 'commodity'
  economicImpact: 'transformational' | 'catalytic' | 'significant' | 'direct-only'
}

export function calculateSectorPipeline(opps: Opportunity[]): SectorPipeline[] {
  // Group opportunities by display sector
  const sectorGroups: Record<string, { total: number; count: number; config: typeof SECTOR_MAPPING[keyof typeof SECTOR_MAPPING] }> = {}
  
  opps.forEach(opp => {
    const config = SECTOR_MAPPING[opp.sector]
    const displayName = config.displayName
    
    if (!sectorGroups[displayName]) {
      sectorGroups[displayName] = {
        total: 0,
        count: 0,
        config,
      }
    }
    
    sectorGroups[displayName].total += opp.investmentSize
    sectorGroups[displayName].count += 1
  })
  
  // Convert to array and sort by pipeline size
  return Object.entries(sectorGroups)
    .map(([sector, data]) => ({
      sector,
      pipeline: Math.round(data.total / 1000), // Convert to billions
      projects: data.count,
      color: data.config.color,
      quality: data.config.quality,
      economicImpact: data.config.economicImpact,
    }))
    .sort((a, b) => b.pipeline - a.pipeline)
}






